  import img_home_today_amount from '@/assets/images/home_today_amount.png';
  import img_home_yesterday_amount from '@/assets/images/home_yesterday_amount.png';
  export default {
    name: 'home',
    data() {
      return {
        pickerOptions: {
          shortcuts: [{
            text: '最近一周',
            onClick(picker) {
              const end = new Date();
              let start = new Date();
              start.setFullYear(2018);
              start.setMonth(10);
              start.setDate(1);
              end.setTime(start.getTime() + 3600 * 1000 * 24 * 7);
              picker.$emit('pick', [start, end]);
            }
          }, {
            text: '最近一月',
            onClick(picker) {
              const end = new Date();
              let start = new Date();
              start.setFullYear(2018);
              start.setMonth(10);
              start.setDate(1);
              end.setTime(start.getTime() + 3600 * 1000 * 24 * 30);
              picker.$emit('pick', [start, end]);
            }
          }]
        },
        orderCountDate: '',
        chartSettings: {
          xAxisType: 'time',
          area:true,
          axisSite: { right: ['order_amount']},
        labelMap: {'order_count': '订单数量', 'order_amount': '订单金额'}},
        order_data:{
          'month_order_num':0,
          'month_last_change':0,
          'week_order_num':0,
          'week_last_change':0,
          'month_sale_num':0,
          'month_last_sale_change':0,
          'week_sale_num':0,
          'week_last_sale_change':0,
        },
        chartData: {
          columns: [],
          rows: []
        },
        home_data: {
          'today_order_num':0,
          'today_all_price':0,
          'all_price':0,
          'product_all_num':0,
          'product_nervous_num':0,
          'product_publish_num':0,
          'product_wait_num':0,
          'today_user_num':0,
          'yesterday_user_num':0,
          'month_user_num':0,
          'user_num':0,
        },
        loading: false,
        dataEmpty: false,
        img_home_order,
        img_home_today_amount,
        img_home_yesterday_amount
      }
    },
    created(){
      this.initOrderCountDate();
      this.getData();
    },
    methods:{
      handleDateChange(){
        this.orderDataChange();
      },
      initOrderCountDate(){
        let start = new Date();
        start.setFullYear(2018);
        start.setMonth(10);
        start.setDate(1);
        const end = new Date();
        end.setTime(start.getTime() + 1000 * 60 * 60 * 24 * 7);
        this.orderCountDate=[start,end];
      },
      getData(){
        this.loading = true;
        fetchHome().then(response => {
          this.home_data = response;
        }).catch(err=> { console.log(err) });
        let sendData=[];
        this.orderCountDate.forEach(v=>{
            let nowData=new Date(v);
            sendData.push(parseInt(nowData.getTime()/1000));
        });
        fetchOrder({'start_time':sendData[0],'end_time':sendData[1]}).then(response => {
          this.order_data=response.order_data;
          setTimeout(() => {
            this.chartData = {
              columns: response.columns,
              rows: response.list
            };
            this.dataEmpty = false;
            this.loading = false
          }, 1000)
        }).catch(err=> { console.log(err) });
      },
      orderDataChange(){
        let sendData=[];
        this.orderCountDate.forEach(v=>{
          let nowData=new Date(v);
          sendData.push(parseInt(nowData.getTime()/1000));
        });
        fetchOrder({'start_time':sendData[0],'end_time':sendData[1]}).then(response => {
          this.order_data=response.order_data;
          setTimeout(() => {
            this.chartData = {
              columns: response.columns,
              rows: response.list
            };
            this.dataEmpty = false;
            this.loading = false
          }, 1000)
        }).catch(err=> { console.log(err) });
      }
    }
  }
</script>

